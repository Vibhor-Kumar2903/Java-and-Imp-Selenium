package interview;
//Encapsulated Class
public class Salary {
	private int testSal;
	private int devlopSal;
	
	public void setTestSalary(int sal) {
		testSal = sal;
	}
	
	public void setDevlopSalary(int sal) {
		devlopSal = sal;
	}
	
	public int getTestSalary() {
		return testSal;
	}
	
	public int getDevlopSalary() {
		return devlopSal;
	}
}